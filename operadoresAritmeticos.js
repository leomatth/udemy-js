alert("Operadores Aritméticos e de Atribuição executados com sucesso!");

// Operadores Aritméticos
let a = 5;  
let b = 3;

// Suma
let soma = a + b;
console.log("Soma:", soma); // 8

// Subtração
let subtracao = a - b;
console.log("Subtração:", subtracao); // 2 

// Multiplicação
let multiplicacao = a * b;
console.log("Multiplicação:", multiplicacao); // 15

// Divisão
let divisao = a / b;
console.log("Divisão:", divisao); // 1.6666666666666667

// Módulo (resto da divisão)
let modulo = a % b;
console.log("Módulo:", modulo); // 2

// Exponenciação
let exponenciacao = a ** b;
console.log("Exponenciação:", exponenciacao); // 125

// Incremento
a++;
console.log("Incremento de a:", a); // 6

// Decremento
b--;
console.log("Decremento de b:", b); // 2

// Operadores de atribuição
let x = 10;
x += 5; // x = x + 5
console.log("Atribuição com adição:", x); // 15

x -= 3; // x = x - 3
console.log("Atribuição com subtração:", x); // 12

x *= 2; // x = x * 2
console.log("Atribuição com multiplicação:", x); // 24

x /= 4; // x = x / 4
console.log("Atribuição com divisão:", x); // 6

x %= 2; // x = x % 2
console.log("Atribuição com módulo:", x); // 0

x **= 3; // x = x ** 3
console.log("Atribuição com exponenciação:", x); // 0


console.log("Fim do script de operadores aritméticos.");